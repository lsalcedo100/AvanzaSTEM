export type EvidenceItem = { key: keyof Evidence; question: string; good: (e: Evidence) => boolean; describe: (e: Evidence) => string }

/** The seven investigation questions, each with how to read the evidence. */
export const EVIDENCE_ITEMS: EvidenceItem[] = [
  { key: "publisher", question: "Who published it?", good: (e) => /official|named|district|club/i.test(e.publisher), describe: (e) => `Published by: ${e.publisher}.` },
  { key: "originalSourceAvailable", question: "Is the original source available?", good: (e) => e.originalSourceAvailable, describe: (e) => (e.originalSourceAvailable ? "An original source can be found." : "No original source can be found.") },
  { key: "dateCorrect", question: "Is the date correct?", good: (e) => e.dateCorrect === true, describe: (e) => (e.dateCorrect === true ? "The date checks out against the original." : e.dateCorrect === false ? "The date does not match the original." : "The date can't be checked.") },
  { key: "captionMatchesContext", question: "Does the caption match the original context?", good: (e) => e.captionMatchesContext, describe: (e) => (e.captionMatchesContext ? "The caption matches the original context." : "The caption changes or misstates the original context.") },
  { key: "hasEvidence", question: "Is there evidence?", good: (e) => e.hasEvidence, describe: (e) => (e.hasEvidence ? "Supporting evidence is provided." : "No supporting evidence is provided.") },
  { key: "independentConfirmation", question: "Do independent reliable sources confirm it?", good: (e) => e.independentConfirmation, describe: (e) => (e.independentConfirmation ? "Independent reliable sources confirm it." : "No independent source confirms it.") },
  { key: "asksForEmotionFirst", question: "Does it push an emotional reaction before evidence?", good: (e) => !e.asksForEmotionFirst, describe: (e) => (e.asksForEmotionFirst ? "It pushes an emotional reaction before showing evidence." : "It does not rush you emotionally.") },
]

/** Counts how many of the seven checks look healthy for a post. */
export function evidenceScore(post: Post): { good: number; total: number } {
  return { good: EVIDENCE_ITEMS.filter((it) => it.good(post.evidence)).length, total: EVIDENCE_ITEMS.length }
}

