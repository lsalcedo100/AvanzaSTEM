export type AppealField = { id: string; prompt: string; hint: string }

export const APPEAL_FIELDS: AppealField[] = [
  { id: "disclosure", prompt: "How does a user learn that AI influenced the decision?", hint: "e.g., a note shown with the result." },
  { id: "requestExplanation", prompt: "How does the user request an explanation?", hint: "e.g., a 'Why this?' button or a form." },
  { id: "reviewer", prompt: "Who reviews the appeal?", hint: "e.g., a teacher or a named staff member — a person, not the AI." },
  { id: "correctData", prompt: "How is incorrect data corrected?", hint: "e.g., the user can edit or flag wrong info." },
  { id: "documentation", prompt: "How is the final decision documented?", hint: "e.g., a short record of what was decided and why." },
  { id: "override", prompt: "When can the AI result be overridden?", hint: "e.g., the human reviewer can always override it." },
]

/** An appeal design is complete when every required part has a real answer. */
export function appealComplete(answers: Record<string, string>): { complete: boolean; missing: string[] } {
  const missing = APPEAL_FIELDS.filter((f) => (answers[f.id] ?? "").trim().length < 3).map((f) => f.id)
  return { complete: missing.length === 0, missing }
}
