export type EthicsFeedback = {
  concerns: string[]
  strengths: string[]
  /** A soundness read: does the decision + safeguards fit the stakes? */
  soundness: "well-reasoned" | "needs-more-safeguards" | "reconsider"
}

/**
 * Evaluates an ethics decision WITHOUT declaring one choice "the" right answer. It
 * reasons about whether the chosen safeguards fit the scenario's stakes and key
 * risks, and returns structured, explanatory feedback.
 */
export function evaluateEthics(scenario: EthicsScenario, decision: Decision, safeguards: SafeguardId[]): EthicsFeedback {
  const chosen = new Set(safeguards)
  const concerns: string[] = []
  const strengths: string[] = []

  const missingKey = scenario.keySafeguards.filter((s) => !chosen.has(s))
  for (const s of scenario.keySafeguards.filter((s) => chosen.has(s))) {
    strengths.push(`You included an important safeguard: “${SAFEGUARDS.find((x) => x.id === s)!.label}”.`)
  }

  const approvesUse = decision === "approve" || decision === "approve-safeguards" || decision === "limited-pilot"

  if (scenario.stakes === "high") {
    if (decision === "approve") concerns.push("This is a high-stakes system, but plain “Approve” adds no safeguards. High-stakes AI needs human oversight and a way to appeal.")
    if (approvesUse && !chosen.has("human-review")) concerns.push("A high-stakes decision should have a person review results before they take effect.")
    if (approvesUse && !chosen.has("appeal")) concerns.push("People affected by a high-stakes decision need a way to appeal or correct it.")
  }

  if (approvesUse) {
    for (const s of missingKey) concerns.push(`Consider adding: “${SAFEGUARDS.find((x) => x.id === s)!.label}”, which matters for this scenario.`)
  }

  if (decision === "reject" && scenario.stakes === "low") {
    concerns.push("Rejecting outright may be stricter than needed here — safeguards might make a low-stakes tool acceptable. Explain your reasoning.")
  }

  if (decision === "reject" && scenario.id === "eth-attention") {
    strengths.push("Rejecting constant camera monitoring is a defensible call — sometimes the responsible choice is not to use AI.")
  }
  if (decision === "require-human-review" && scenario.stakes === "high") {
    strengths.push("Requiring human review before use fits a high-stakes system.")
  }

  let soundness: EthicsFeedback["soundness"] = "well-reasoned"
  if (concerns.some((c) => /needs|should|need a way/i.test(c)) && scenario.stakes === "high" && approvesUse && (!chosen.has("human-review") || !chosen.has("appeal"))) {
    soundness = "reconsider"
  } else if (approvesUse && missingKey.length > 1) {
    soundness = "needs-more-safeguards"
  }
  if (concerns.length === 0) strengths.push("Your decision and safeguards fit the stakes of this scenario.")

  return { concerns, strengths, soundness }
}

