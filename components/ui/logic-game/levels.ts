export function getLogicLevels(language: Language): Level[] {
  if (language === "en") return LEVELS
  const localized = LOCALIZED_LEVEL_TEXT[language]
  return LEVELS.map((level) => {
    const text = localized[level.id]
    return text ? { ...level, ...text } : level
  })
}

